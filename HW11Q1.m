function [] = HW11Q1()
  A = [-1,2,0;5,1,-2;0,4,-3];
  n = 3;
  L = zeros(n);
  U = eye(n);
  L(1,1) = A(1,1)
  L(2,1) = A(2,1)
  U(1,2) = A(1,2)
  for k = 2:n-1
    L(k,k) = A(k,k) - L(k,k-1)*U(k-1,k)
    L(k+1,k) = A(k+1,k)
    U(k,k+1) = A(k,k+1)/L(k,k)
  end
  
  %The matrices U and L are:
  % U =  1.00000   2.00000   0.00000
  %      0.00000   1.00000   0.22222
  %      0.00000   0.00000   1.00000
  % L =  -1   0   0
  %       5  -9   0
  %       0   4   0
end